public class Person {
    private String fName;
    private String lName;
    private int age;

    public Person(String fName, String lName, int age) {
        this.fName = fName;
        this.lName = lName;
        this.age = age;
    }

    @Override
    public String toString() {
        return "Person{" +
                "fName='" + fName + '\'' +
                ", lName='" + lName + '\'' +
                ", age=" + age +
                '}';
    }

    public String getfName() {
        return fName;
    }

    public String getlName() {
        return lName;
    }

    public int getAge() {
        return age;
    }

    public String getFio(){
        StringBuilder sb = new StringBuilder();
        String firstName= fName!=null && !fName.isEmpty()?fName.substring(0,1):"";
        return sb.append(firstName).append(".").append(lName).toString();
    }

}
