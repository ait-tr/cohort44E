public class Main4 {
    public static void main(String[] args) {

        // while
        int i=0;
        while (i < 3){
            System.out.println("hello: " + i);
            i=i+1;
        }
        System.out.println("stop: " + i);

        System.out.println("------------------------------------------");




        // for
        for (i=0; i<3; i=i+1){ ///      for ( начальные значения;  условие    ; изменение ){}
            System.out.println("hello: " + i);
        }
        System.out.println("stop: " + i);


    }
}
